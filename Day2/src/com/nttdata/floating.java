package com.nttdata;

public class floating {
	public static void main(String[] args) {
		int i=20;
		int j=i++%5;
		System.out.println(" value of i " +i);
		System.out.println(" value of j " +j);
		
		int k=20;
		int l=++k%5;
		System.out.println(" value of k " +k);
		System.out.println(" value of l " +l);
		
		
	}
}
